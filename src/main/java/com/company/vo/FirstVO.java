package com.company.vo;

public class FirstVO {
	int idx;
	String str;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	@Override
	public String toString() {
		return "FirstVO [idx=" + idx + ", str=" + str + "]";
	}
	
}
